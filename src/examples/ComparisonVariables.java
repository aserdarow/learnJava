package examples;

public class ComparisonVariables {
    public static void main(String[] args) {
        // Операторы сравнения

        int a = 5;
        int b = 7;

        System.out.println(a > b);  // False
        System.out.println(a < b);  // True
        System.out.println(a >= b);  // False
        System.out.println(a <= b);  // True

        System.out.println(a == b);  // False
        System.out.println(a != b);  // True

    }
}
